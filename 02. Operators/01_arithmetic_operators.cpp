// C++ Program to demonstarte Arithmetic Operators

#include<iostream>
using namespace std;

int main(){
	int a = 80;
	int b = 10;
	
	cout << "Sum = " << a+b << endl;
	cout << "Difference = "<< a-b << endl;
	cout << "Multiplication = " << a*b << endl;
	cout << "Division = " << a/b << endl;
	cout << "Remainder = " << a%b << endl;
	return 0;
}
